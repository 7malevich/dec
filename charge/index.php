<p>
	Для удобства автолюбителей зарядные станции установили на парковках супермаркетов и торгово-развлекательных центров, 
у офисов Единых расчетных центров ДЭК РусГидро и на традиционных автозаправках. </p>
<p>
РусГидро как энергокомпания, ответственная за развитие энергетики Дальнего Востока, стремится обеспечить потребителей современной услугой зарядки 
электротранспорта, сделать использование электромобиля доступным, а поездки между населенными пунктами простыми и быстрыми.
</p>

