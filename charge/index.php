Станции оборудованы зарядочными портами для японских и европейских автомобилей (DC) 50 кВт (11 руб./кВт·ч), «медленным» разъемом Type 1/Type 2 (AC), 
	22/43* кВт (8 руб./кВт·ч) <i>(22 или 43 кВт мощность ЭЗС по переменному току зависит от типа оборудования)</i>. Можно заряжать две машины одновременно.
