<p>Общее количество электроэнергии, поступающее через входное распределительное устройство во внутридомовые сети дома, фиксируется общедомовым прибором учета, а затем поступает в квартиры и места общего пользования. Электрическая энергия, поступившая в квартиру, измеряется индивидуальными приборами учета.</p>

<p>Вся остальная энергия расходуется для работы приборов освещения помещений общего пользования и для работы электрического оборудования, входящего в состав общего имущества в многоквартирном доме.</p>

<p>В соответствии с Постановлением Правительства РФ от&nbsp;13 августа 2006 г. № 491 в состав общего имущества включаются:</p>

<p>а) помещения в многоквартирном доме, не являющиеся частями квартир и предназначенные для обслуживания более одного жилого и (или) нежилого помещения в этом многоквартирном доме: межквартирные лестничные площадки, лестницы, лифты, лифтовые и иные шахты, коридоры, колясочные, чердаки, технические этажи (включая построенные за счет средств собственников помещений встроенные гаражи и площадки для автомобильного транспорта, мастерские, технические чердаки) и технические подвалы, в которых имеются инженерные коммуникации, иное обслуживающее более одного жилого и (или) нежилого помещения в многоквартирном доме оборудование (включая котельные, бойлерные, элеваторные узлы и другое инженерное оборудование);</p>

<p>б) иные объекты, предназначенные для обслуживания, эксплуатации и благоустройства многоквартирного дома, включая трансформаторные подстанции, тепловые пункты, предназначенные для обслуживания одного многоквартирного дома, коллективные автостоянки, гаражи, детские и спортивные площадки, расположенные в границах земельного участка, на котором расположен многоквартирный дом.</p>

