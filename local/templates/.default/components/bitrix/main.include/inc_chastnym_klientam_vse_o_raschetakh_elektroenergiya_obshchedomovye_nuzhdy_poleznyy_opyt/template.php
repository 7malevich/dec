<h4>ЗАЩИТИТЕ СЕБЯ ОТ РАСХИТИТЕЛЕЙ ЭЛЕКТРОЭНЕРГИИ И ВЫСОКИХ ПЛАТЕЖЕЙ ЗА ОБЩЕДОМОВЫЕ НУЖДЫ!</h4>
<p>Уважаемый абонент!
</p>
<p>Если в квитанции за электроэнергию сумма платежа за электроснабжение общедомовых нужд ("Доля ОДН"), выше, чем ваше индивидуальное потребление, это один из признаков наличия неучтенного хищения электроэнергии, а именно самовольного подключения к электроснабжению в обход установленного прибора учета. Если не выявить виновника, то обязанность по оплате похищенной электроэнергии возлагается на ВСЕХ жильцов, в том числе и на добросовестных потребителей и плательщиков.
</p>
<p>Лица, виновные в хищении электроэнергии, должны нести ответственность, установленную законом. Сетевые и управляющие организации постоянно проводят проверки, выявляя факты безучетного потребления электроэнергии среди абонентов компании. Чтобы сделать эту работу еще более эффективной, мы должны объединить наши усилия.
</p>
<p>При обнаружении фактов воровства электроэнергии Вы можете обратиться в свою управляющую организацию (УК, ТСЖ и т.п.) с просьбой провести проверку в вашем доме. Также вы можете обратиться в органы УВД или к участковому инспектору.
</p>