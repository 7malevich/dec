<h4>Если "ОДН" превышает сумму за индивидуальное потребление, что делать?</h4>
<p>Отметим, что оплата по счетчику – самый достоверный способ расчета за израсходованную электроэнергию, но при определенных условиях. Что влияет на объем расхода электроэнергии?
</p>
<p>Во-первых, наличие в доме магазинов или иных коммерческих организаций. Предприниматели, чьи офисы находятся в жилых домах, могут быть недобросовестными потребителями электроэнергии. Они устанавливают на домах световые вывески, рекламу, а каким образом они подключены? Кто платит за эту электроэнергию?
</p>
<p>Во-вторых, типичный пример: киоски, хозяева которых присоединяют свои провода напрямую к энергосистеме находящегося неподалеку жилого дома. В результате, общедомовой счетчик учитывает расходы коммерческих структур, а оплачивают эти расходы как правило жители. Что нужно делать в такой ситуации? Организация, уполномоченная собственниками (управляющая компания, правление ТСЖ или ЖСК) должна провести инвентаризацию общедомового электрооборудования и выяснить схемы подключения коммерческих структур.
</p>
<p>Только после наведения порядка в электросетях своего дома, собственники могут быть уверены, что платят за фактически израсходованную электроэнергию.
</p>
