<p class="note"> <strong>Примечания:</strong> </p>
 
<p class="note"> 1. Зоны суток: </p>
 
<p class="note"> Пиковая зона: 
  <br />
 январь, февраль - с 9 ч. до 11 ч. и с 19 ч. до 23 ч.;
  <br />
 март, октябрь - с 9 ч. до 12 ч. и с 20 ч. до 23 ч.;
  <br />
 апрель - с 10 ч. до 12 ч. и с 21 ч. до 24 ч.;
  <br />
 май-август - с 10 ч. до 12 ч. и с 22 ч. до 24 ч.;
  <br />
 сентябрь - с 10 ч. до 12 ч. и с 21 ч. до 23 ч.;
  <br />
 ноябрь, декабрь - с 9 ч. до 11 ч. и с 18 ч. до 22 ч. </p>
 
<p class="note"> Полупиковая зона: 
  <br />
 январь, февраль - с 8 ч. до 9 ч.; с 11 ч. до 19 ч.; с 23 ч. до 24 ч.;
  <br />
 март, октябрь - с 8 ч. до 9 ч.; с 12 ч. до 20 ч.; с 23 ч. до 24 ч.;
  <br />
 апрель - с 8 ч. до 10 ч.; с 12 ч. до 21 ч.;
  <br />
 май-август - с 8 ч. до 10 ч.; с 12 ч. до 22 ч.;
  <br />
 сентябрь - с 8 ч. до 10 ч.; с 12 ч. до 21 ч.; с 23 ч. до 24 ч.;
  <br />
 ноябрь, декабрь - с 8 ч. до 9 ч.; с 11 ч. до 18 ч.; с 22 ч. до 24 ч. </p>
 
<p class="note"> Ночная зона - с 24 ч. до 8 ч. </p>
 
<p class="note"> В воскресные и праздничные дни пиковая зона равна полупиковой, полупиковая &ndash; ночной.
  <br />
 Пиковая зона субботних дней оплачивается по пиковой тарифной ставке рабочего дня, уменьшенной с учетом коэффициента К=0,7, но не ниже полупиковой тарифной ставки рабочего дня </p>
 
<p class="note"> 2. Места общего пользования- работа лифтов, лифтовые диспетчерские, освещение лестничных клеток, работа насосов, освещение придомовой территории и др. </p>
 
