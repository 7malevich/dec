<h3>Примеры расчета оплаты </h3>
 
<p> <strong>Пример 1:</strong> В двухкомнатной квартире с электроплитой проживает три человека. Норматив потребления в данном случае составляет 91 кВт/ч на человека. Таким образом, их суммарный норматив на троих составляет 273 кВт/ч. По тарифу 2009 г. это соответствует 273 * 1,40 = 382,2 руб. </p>
 
<p> <strong>Пример 2:</strong> В доме из трех комнат без электроплиты проживает два человека. Норматив потребления в данном случае составляет 100 кВт/ч на человека. Таким образом, их суммарный норматив на двоих составляет 200 кВт/ч. По тарифу 2009 г. это соответствует 200 * 2 = 400 руб. 
