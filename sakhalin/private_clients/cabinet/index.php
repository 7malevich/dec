<h2><a id="faq" name="faq"></a>Вопросы и ответы о сервисе «Личный кабинет»:</h2>

<div class="spoiler folded">
  <h4 class="sp-hdr">В течение какого времени выполняется загрузка информации по лицевому счету после регистрации в сервисе «Личный кабинет»?</h4>
</div>
<div class="spoiler-text">
	<p>Информация по лицевому счету отражается в сервисе «Личный кабинет» клиента ПАО «ДЭК» сразу же после прохождения регистрации.</p>
</div>

<div class="spoiler folded">
  <h4 class="sp-hdr">Могу ли я в одном Личном кабинете иметь несколько лицевых счетов?</h4>
</div>
<div class="spoiler-text">
	<p>Да, такая возможность реализована. Для этого нужно воспользоваться функцией «Добавить лицевой счет». Подробная информация по добавлению лицевого счета представлена в инструкции по использованию сервиса «Личный кабинет».</p>
</div>

<div class="spoiler folded">
  <h4 class="sp-hdr">Через сколько дней в Личном кабинете клиента ПАО «ДЭК» отражается произведенная оплата?</h4>
</div>
<div class="spoiler-text">
	<p>В сервис «Личный кабинет клиента» оплата загружается ежедневно.</p>
</div>

<div class="spoiler folded">
  <h4 class="sp-hdr">Когда в сервисе «Личный кабинет» появляются квитанции на оплату?</h4>
</div>
<div class="spoiler-text">
	<p>Платежные документы загружаются в сервис «Личный кабинет» после окончания отчетного периода (календарного месяца), т.е. в начале месяца, следующего за отчетным. Возможность получения платежных документов через сервис «Личный кабинет» появляется у клиентов раньше, чем доставка квитанций на бумажных носителях через почтовые ящики.</p>
</div>

<div class="spoiler folded">
  <h4 class="sp-hdr">Можно в сервисе «Личный кабинет» увидеть информацию о сроке истечения межповерочного интервала прибора учета?</h4>
</div>
<div class="spoiler-text">
	<p>В Личном кабинете клиента информацию о сроке истечения межповерочного интервала можно увидеть в разделе «Показания счетчиков». В таблице указан номер прибора учета, предыдущие и текущие показания, дата предыдущих показаний, а также срок поверки.</p>
</div>


