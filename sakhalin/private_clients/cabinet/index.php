<h2>Вопросы и ответы о мобильном приложении:</h2>

<div class="spoiler folded">
  <h4 class="sp-hdr">Для чего может понадобиться перезагрузка мобильного приложения ПАО&nbsp;«ДЭК»?</h4>
</div>
<div class="spoiler-text">
	<p>Старая версия мобильного приложения ПАО «ДЭК»  может стать причиной возникновения ошибок и сбоев в работе, таких как: передача  показаний приборов учета, своевременное получение актуальной информации и т.п. Решить  проблему сможет обновление сервиса, которое осуществляется автоматически, если  только данная опция не отключена. В случае, если обновления отключены, нужно  выполнить перезагрузку вручную. </p>
	<p>Перезагрузка  способна решить большинство проблем, которые могут возникнуть в работе  интерактивного сервиса. Основные причины, по которым может понадобится такая  процедура:</p>
    <ul>
      <li>«зависание»  приложения: невозможно войти в приложение, не выполняются стандартные действия;</li>
      <li>очень  медленная работа программы (задачи выполняются в течение длительного времени);</li>
      <li>не  осуществляется отправка показаний/обращений. Такая ситуация может возникнуть в  том случае, если прервано соединение с интернетом. Если подключение активно и  стабильно, но проблема с отправкой сохранилась, проблема решается  перезагрузкой.</li>
    </ul>
  <div class="alert alert-warning">
      <strong>Важно:</strong> если последующая процедура перезагрузки не приносит результата, причиной может  быть сбой в работе самого гаджета или его операционной системы.
  </div>
    <p>Обойтись  без перезапуска приложения можно, осуществив выключение и включение мобильного  устройства. Это поможет «очистить» систему от ошибок, устранив мелкие ошибки.</p>
    <p> Перезапуск  сервиса мобильного приложения ПАО «ДЭК» будет зависеть от операционной системы,  которой управляется устройство.</p>
