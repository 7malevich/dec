<h2>Спортивно-оздоровительная работа</h2>
<p>Традиция проведения спортивных  соревнований между коллективами ПАО «ДЭК» имеет многолетнюю историю. 
  Ежегодно на Дальнем Востоке проходят  спартакиады ДЭК по зимним и летним видам спорта, спортивные состязания по  отдельным видам спорта, туристические слёты. В начале каждого года составляется  и утверждается календарь спортивных мероприятий, в которых неизменно участвую  команды всех предприятий.</p>
<p> Команды ПАО «ДЭК» неизменно  становятся участниками сборной защищающей честь дальневосточной энергетики на  всероссийских спартакиадах проводимых под эгидой ПАО  «РусГидро». А так же принимают участие в  межведомственных спартакиадах, турнирах по футболу, краевых спортивных  мероприятиях, сдаче норм ГТО.</p>

<h2>Корпоративный отдых ПАО «ДЭК»</h2>
<p>На сегодняшний день компания уделяет  значительное внимание организации отдыха сотрудников и членов их семей. ПАО  «ДЭК» совместно с Общественной организацией «Всероссийский Электропрофсоюз»:</p>
<ul>
  <li>Организовывает для своих работников поездки на базы отдыха;</li>
  <li>Обеспечивает путевками в детские лагеря детей сотрудников;</li>
  <li>Предоставляет билеты на различные детские праздники;</li>
  <li>Подготавливает подарки детям сотрудников к началу учебного года.</li>
</ul>
<p>Ежегодно для представителей филиалов  ПАО «ДЭК» проводится туристический слет.</p>

<h2>Трудовые соревнования</h2>
<p>В целях повышения творческой  активности коллективов, мобилизации персонала на решение поставленных задач,  достижения высоких производственных и финансово-экономических результатов ПАО  «ДЭК» организует и проводит трудовые соревнования среди своих сотрудников. </p>
<p>Трудовые соревнования проводятся  между филиалами ПАО «ДЭК» и помогают оценить уровень профессиональной  подготовки персонала филиалов и определить наиболее квалифицированных  сотрудников.</p>
<p> В рамках трудовых соревнований также  регулярно проводится конкурс профессионального мастерства на звание «Лучший по  профессии». Организация трудовых соревнований  проходит таким образом, чтобы охватить максимально возможное количество  работников ПАО «ДЭК» и филиалов энергокомпании. Победителю вручается  переходящий кубок.</p>

