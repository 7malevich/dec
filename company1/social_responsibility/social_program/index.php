<h2>Добровольное медицинское страхование</h2>
<p>Социальное партнёрство – один из  основных принципов организации трудовых отношений в ПАО «ДЭК». Сотрудники  энергокомпании имеют право на компенсацию расходов на лечение, предусмотренные  договором добровольного медицинского страхования работников ПАО «ДЭК» со  страховой компанией АО «СОГАЗ» на 2017-2018 годы.<br />
  Дополнительная квалифицированная  медицинская помощь работникам в связи с заболеванием, осуществляется по  следующим программам:</p>
<ul>
  <li>амбулаторно-поликлиническая помощь;</li>
  <li>стационарная помощь;</li>
  <li>вакцинация;</li>
  <li>в исключительных случаях, реабилитационно - восстановительное  лечение после перенесенного заболевания и др.</li>
</ul>

