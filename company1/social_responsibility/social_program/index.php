  <li>Добровольное медицинское страхование;</li>
  <li>Спортивно-оздоровительная работа;</li>
  <li>Культурно-массовая работа;</li>
  <li>Организация отдыха работников и членов их семей;</li>
  <li>Благотворительная деятельность;</li>
  <li>Планирование и анализ социальных выплат;</li>
</ul>
<p><strong>Основными  функциями социальной политики ПАО «ДЭК» являются:</strong> </p>
<ul>
  <li>Разработка и согласование программ, положений и локальных актов  (коллективные договоры, социальные программы, программы негосударственного  пенсионного обеспечения, программы добровольного медицинского страхования и др.)</li>
  <li>Планирование затрат на социальное обеспечение (расчёт социальных  льгот и гарантий, расчёт прочих выплат на социальное обеспечение)</li>
