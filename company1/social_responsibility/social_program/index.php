<p>Социальная программа ПАО «ДЭК»  направлена на формирование благоприятных условий труда и жизни работников и  содействует социально-экономическому развитию регионов присутствия компании.</p>
<p><strong>Цели  социальной программы ПАО «ДЭК»:</strong></p>
<ul>
  <li>Регулирование социально-трудовых отношений;</li>
  <li>Обеспечение контроля за планированием и отчётностью по выплатам  социального характера;</li>
  <li>Обеспечение негосударственного пенсионного обеспечения работников;</li>
  <li>Обеспечение добровольного медицинского страхования работников;</li>
  <li>Обеспечение спортивно-оздоровительной работы и приобщение  сотрудников к занятиям спортом;</li>
  <li>Обеспечение культурно-массовой работы, реализация творческого  потенциала сотрудников;</li>
  <li>Оказание содействия в организации отдыха работников и их детей;</li>
  <li>Обеспечение благотворительной деятельности компании и её  позиционирование, как социально-ответственной компании;</li>
  <li>Оказание консультационной помощи по вопросам социального  характера.</li>
</ul>
<p><strong>Приоритетные  направления социальной программы ПАО «ДЭК»:</strong></p>
