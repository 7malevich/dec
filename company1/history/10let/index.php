<p class="justp">Учитывая, что наиболее приоритетным направлением деятельности компании является реализация клиентоориентированной политики, направленной на улучшение взаимодействия с клиентами компании, в  октябре 2007 года сразу во всех регионах зоны ответственности энергокомпании: в Приморье, Хабаровском крае, Амурской и Еврейской автономной областях начал работу Единый информационный центр ПАО &laquo;ДЭК&raquo; для потребителей электроэнергии Дальнего Востока. </p>

<div style="background:url(/images/design2016/article/eic.jpg) no-repeat; width:380px; height:220px; margin: 20px auto 5px auto; padding: 28px 200px 0 25px">
	<p class="justp">Единый информационный центр ПАО &laquo;ДЭК&raquo; - это многоканальная телефонная линия, на которой вы можете задать любые интересующие вас вопросы, касающиеся электроснабжения.</p>
    <p class="justp"><strong>Во всех регионах номер телефонной линии единый: 8 800 100 3 777. <br />
Звонок из любого региона бесплатный. </strong></p>
</div>

<p class="justp">В каждом из филиалов работу информационного центра обеспечивают квалифицированные специалисты, которые прошли профессиональную подготовку. Днём на звонки потребителей отвечают операторы линии, ночью обращения и вопросы записываются на автоответчик.</p>
<p class="justp">Нам важно ваше мнение. Именно с целью налаживания крепкого диалога между компанией и нашими потребителями и была открыта единая телефонная линия для потребителей электроэнергии Дальнего Востока. </p>
<p class="justp">Любой житель любого уголка нашего региона может позвонить и задать свои вопросы, высказать пожелания и может быть уверен, что его вопросы и пожелания будут услышаны и учтены.</p>
</div>


