<h2>Совет директоров </h2>
 
<p>Совет директоров осуществляет общее руководство  деятельностью Общества, за исключением решения вопросов, отнесенных ГК РФ,  Федеральным законом «Об акционерных обществах» и Уставом ПАО «ДЭК» к  компетенции Общего собрания акционеров Общества. </p>
<p>К компетенции  Совета директоров ПАО «ДЭК» относятся вопросы:</p>
<ul>
  <li>Вопросы  финансово-хозяйственной деятельности Общества</li>
  <li>Вопросы, связанные с  Генеральным директором Общества</li>
  <li>Вопросы, связанные с  подготовкой и проведением Общего собрания акционеров</li>
  <li>Согласие на совершение  сделок Общества</li>
  <li>Вопросы, связанные с  ценными бумагами Общества</li>
  <li>Утверждение внутренних  документов Общества</li>
  <li>Обеспечение деятельности  Совета директоров</li>
  <li>Вопросы, связанные с  участием Общества в иных организациях</li>
  <li>Иные вопросы</li>
</ul>
<p>Перечень вопросов, относящихся к компетенции Совета директоров  Общества, а также число голосов, необходимое для принятия решения по ним,  определены Уставом ПАО «ДЭК» (пп. 10.2.1 – 10.2.59 в редакции с учетом  изменений, внесенных в 2022 году по решению годового Общего собрания  акционеров, протокол от 10.06.2022 №21).</p>
<p>Вопросы, отнесенные к компетенции Совета  директоров, не могут быть переданы на решение единоличного исполнительного  органа.</p>
<p> Порядок деятельности Совета директоров Общества определяется законодательством РФ, Уставом ПАО «ДЭК» и внутренним  документом - Положением о порядке созыва и  проведения заседаний Совета  директоров Публичного  акционерного общества «Дальневосточная  энергетическая компания» (новая редакция).</p>

