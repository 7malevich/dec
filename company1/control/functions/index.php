<p> Порядок созыва, подготовки и проведения Общего  собрания акционеров Общества  определяется законодательством РФ,  Уставом ПАО «ДЭК» и внутренним документом – Положением о порядке  подготовки и проведения Общего собрания акционеров Публичного акционерного  общества «Дальневосточная  энергетическая компания» (новая  редакция).</p>
