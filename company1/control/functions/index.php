<h2>Общее собрание акционеров </h2>
 
<p>Высшим органом управления Общества является Общее  собрание акционеров. К компетенции  Общего собрания акционеров ПАО «ДЭК» относятся вопросы:</p>
<ul>
  <li>Утверждение Устава, внесение изменений в Устав</li>
  <li>Реорганизация и ликвидация  Общества</li>
  <li>Вопросы, связанные с  образованием органов управления и контроля Общества</li>
  <li>Вопросы  финансово-хозяйственной деятельности Общества</li>
  <li>Вопросы, связанные с  согласием на совершение сделок</li>
  <li>Вопросы, связанные с  уменьшением и увеличением уставного капитала Общества</li>
  <li>Иные вопросы</li>
</ul>
<p>Перечень вопросов, относящихся к компетенции Общего собрания акционеров  Общества, а также число голосов, необходимое для принятия решения по ним,  определены Уставом ПАО «ДЭК» (пп. 9.2.1 – 9.2.31 в редакции с учетом изменений,  внесенных в 2022 году по решению годового Общего собрания акционеров, протокол  от 10.06.2022 №21).</p>
