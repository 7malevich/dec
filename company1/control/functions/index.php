<h2>Единоличный исполнительный орган </h2>
 
<p>К компетенции единоличного исполнительного органа  относятся все вопросы руководства текущей деятельностью Общества, за  исключением вопросов, отнесенных к компетенции Общего собрания акционеров и  Совета директоров.</p>
<p> По решению Общего собрания акционеров полномочия  единоличного исполнительного органа Общества могут быть переданы по договору  управляющей организации или управляющему.</p>
