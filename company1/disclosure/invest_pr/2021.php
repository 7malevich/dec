?><h2>
Информация об инвестиционной программе ПАО «ДЭК» за 2021г. </h2>
<p>
	 Инвестиционная программа ПАО «ДЭК» 2021 года была направлена на исполнение требований Федерального закона от 27.12.2018 №522-ФЗ «О внесении изменений в отдельные законодательные акты Российской Федерации в связи с развитием систем учета электрической энергии (мощности) в Российской Федерации», приведение клиентских офисов в соответствие с требованиями Стандарта обслуживаний клиентов ПАО «ДЭК», повышения качества предоставляемых услуг потребителям электро- и теплоэнергии с использованием цифровых технологий и искусственного интеллекта.
</p>
<p>
	 Инвестиционная программа&nbsp;2021 года утверждена Советом Директоров ПАО «ДЭК» (Протокол № 395 от 18.12.2020) в составе бизнес-плана Общества в объеме капитальных вложений 962,0 млн. руб. (без НДС) и объеме финансирования &nbsp;1147,3 млн. руб. (с НДС).
</p>
<p>
	 По факту освоение капитальных вложений за 2021 год составило 498,4 млн. руб. (без НДС), или&nbsp; 52% от утвержденного плана, фактически профинансировано 590,1 млн. руб. (с НДС), или 51% от утвержденного плана.
</p>
<p>
	 Ввод основных фондов за 2021 г. составил 222,8 млн. руб. (без НДС).
</p>
