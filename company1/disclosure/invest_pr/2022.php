Информация об исполнении инвестиционной программы ПАО «ДЭК» за 2022 г. </h2>
<p>
	 Инвестиционная программа ПАО «ДЭК» 2022 года направлена на исполнение требований Федерального закона от 27.12.2018 №522-ФЗ «О внесении изменений в отдельные законодательные акты Российской Федерации в связи с развитием систем учета электрической энергии (мощности) в Российской Федерации», приведение клиентских офисов в соответствие с требованиями Стандарта обслуживаний клиентов ПАО «ДЭК», повышение качества предоставляемых услуг потребителям электро- и теплоэнергии с использованием цифровых технологий и искусственного интеллекта.
</p>
<p>
	 Инвестиционная программа&nbsp;2022 года утверждена Советом Директоров ПАО «ДЭК» (Протокол № 413 от 06.12.2021) в составе бизнес-плана Общества в объеме капитальных вложений в сумме 1&nbsp;403,88 млн. руб. (без НДС) и объеме финансирования в сумму 1&nbsp;684,66 млн. руб. (с НДС).
</p>
<p>
	 По факту освоение капитальных вложений за 2022 год составило 1&nbsp;048,0 млн. руб. (без НДС), или&nbsp;75% от утвержденного плана, профинансировано 1&nbsp;243,19 млн. руб. (с НДС), или 73,8% от утвержденного плана.
</p>
<p>
	 Ввод основных фондов за 2022 г. составил 486,37 млн. руб. (без НДС).
</p>
