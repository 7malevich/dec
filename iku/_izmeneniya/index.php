<h2 class="sp-hdr">Ответственность УК,  ТСЖ, ЖСК при накоплении задолженности</h2></div>
<div class="spoiler-text">
<p>В соответствии с Постановлением Правительства  Российской Федерации от 13.09.2018 N 1090 «О внесении изменений в некоторые  акты Правительства Российской Федерации по вопросам управления многоквартирными  домами» (п. 4¹ «д»), <strong><em><u>к грубым  нарушениям лицензионных требований относится наличие  у лицензиата</u></em></strong> признанной им или подтвержденной вступившим в законную силу судебным  актом <strong><em><u>задолженности перед ресурсоснабжающей организацией в размере, равном  или превышающем 2 среднемесячные величины обязательств по оплате по договору</u></em></strong> ресурсоснабжения, заключенному в целях обеспечения предоставления собственникам  и пользователям помещения в многоквартирном доме коммунальной услуги  соответствующего вида и (или) приобретения коммунальных ресурсов, потребляемых  при использовании и содержании общего имущества в многоквартирном доме,  независимо от факта последующей оплаты указанной задолженности лицензиатом.</p>
<p> Согласно п. 4² «д» при  повторном совершении лицензиатом в течение 12 месяцев со дня назначения административного  наказания за нарушение лицензионных требований, которое отнесено к грубым  нарушениям лицензионных требований, предусмотренных подпунктом &quot;д&quot;  пункта 4¹ настоящего Положения, <strong><em><u>из реестра лицензий субъекта</u></em></strong> Российской Федерации в соответствии с частью 5.2 статьи 198 Жилищного кодекса  Российской Федерации <strong><em><u>подлежат исключению</u></em></strong> сведения  о многоквартирном доме или многоквартирных домах, в отношении которых такие  грубые нарушения лицензионных требований совершены.</p>
  <p>Данные изменения  вступили в силу с 26.09.2018г.</p>
