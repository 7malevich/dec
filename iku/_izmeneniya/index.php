<h2 class="sp-hdr">Информационный  обмен при прямых договорах</h2></div>
<div class="spoiler-text">
<p>При переходе на «прямые» договоры УК, ТСЖ, ЖСК обязаны:</p>
<ul>
  <li>предоставлять РСО информацию, необходимую для начисления платы за КУ, в том числе  о показаниях ИПУ (при предоставлении показаний в УО, ТСЖ, ЖСК), ОДПУ;</li>
  <li> обеспечивать постоянную готовность инженерных коммуникаций, входящих в состав  общего имущества, инженерных систем;<br />
  </li>
  <li>осуществлять контроль качества коммунальных ресурсов и непрерывности их подачи  до границ МКД;<br />
  </li>
  <li>принимать от жителей обращения о нарушениях качества и (или) непрерывности  предоставления КУ, нарушениях при расчете размера плату КУ;<br />
  </li>
  <li>взаимодействовать с РСО при рассмотрении указанных обращений, проведении  проверки, устранении нарушений;<br />
  </li>
  <li>для осуществления приостановки или ограничения предоставления КУ:
  <ul><li>обеспечивать  РСО доступ к общему имуществу в МКД;</li>
  <li>либо  выполнять действия по соглашению с РСО.<br />
  (ч.11  ст.161 ЖК РФ)</li></ul></li>
</ul>
</div>

<div class="spoiler folded">
<h2 class="sp-hdr">Ответственность УК,  ТСЖ, ЖСК за «двойные» квитанции</h2></div>
<div class="spoiler-text">
<ul>
  <li> если УО, ТСЖ, ЖСК выставляет платежный документ за КУ за период после  прекращения действия договора ресурсоснабжения в целях предоставления КУ, то  она уплачивает потребителям штраф в двукратном размере суммы платежный документ.  Кроме случая, когда нарушение было устранено до внесения платы за КУ по таким платежным  документам;</li>
  <li> в случае внесения платы за КУ на основании указанных платежный документ  потребители признаются надлежаще исполнившими обязанность по внесению платы за  КУ. Задолженность взыскивается РСО с УО, ТСЖ, ЖК.<br />
    (ч.5  ст.3 ФЗ-59 от 03.04.2018)</li>
</ul>
</div>

