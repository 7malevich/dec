<div class="spoiler folded">
<h2 class="sp-hdr">Заключение прямых договоров</h2></div>
<div class="spoiler-text">
<p>В  связи с внесением изменений в Жилищный кодекс РФ <a href="http://www.consultant.ru/document/cons_doc_LAW_294732/" target="_blank">Федеральным законом от  03.04.2018г. №59-ФЗ</a>, устанавливается возможность заключения договоров о  предоставлении коммунальных услуг (далее – КУ) электроснабжения <strong>непосредственно  между ресурсоснабжающей организацией (РСО) и потребителями</strong> (ст. 157 (2)) в отношении электроэнергии,  потребляемой в жилых помещениях (индивидуальное потребление).</p>
<p> При  управлении многоквартирным домом управляющей организацией, товариществом  собственников жилья либо жилищным кооперативом или иным специализированным  потребительским кооперативом коммунальные услуги  предоставляются собственникам РСО в следующих случаях:</p>
<p><strong>1. При принятии  соответствующего решения на общем собрании собственников помещений в  многоквартирном доме:</strong></p>
<p>Обязательные  условия:</p>
<ul>
  <li>  Решение общего собрания собственников должно соответствовать п. 4.4. ч. 2 ст.  44 ЖК РФ;<br />
  </li>
  <li>УК должна предоставить в ресурсоснабжающую организацию копию решения и  подлинник протокола общего собрания собственников помещений в многоквартирном  доме не позднее чем через 10 дней после проведения общего собрания  собственников (ч. 1 ст. 46 ЖК РФ);<br />
  </li>
  <li>дата  заключения договора определяется в решении общего собрания собственников.</li>
</ul>
<p>По решению РСО срок перехода на прямые договоры может  быть перенесен, но не более чем на три календарных месяца. О таком решении РСО  уведомляет лицо – инициатора общего собрания собственников не позднее 5 рабочих  дней со дня получения копий решения и протокола общего собрания собственников  (пп.1 ч.7 ст.157.2 ЖК РФ).</p>
<p><strong>2. При прекращении  заключенного договора вследствие одностороннего отказа РСО:</strong></p>
<p>Основанием  расторжения является наличие задолженности:</p>
<ul>
  <li>равной  или большей 2 среднемесячным начислениям;<br />
  </li>
  <li>признанной  или подтвержденной вступившим в законную силу решением суда;<br />
  </li>
  <li>независимо  от факта последующей оплаты, за исключением полного погашения задолженности до  вступления в законную силу судебного акта.</li>
</ul>
<p> (в  части оплаты ОДН изменения в законодательство не внесены, ОДН всегда оплачивает  только ИКУ).</p>
<p><strong>Среднемесячная  величина = сумма обязательств за двенадцать месяцев, предшествующих дате  направления уведомления об одностороннем отказе от исполнения договора  ресурсоснабжения / 12. </strong></p>
<p>В  случае, если договор действовал менее двенадцати месяцев:<br />
<strong>Среднемесячная  величина = сумма обязательств за период действия договора / количество месяцев  их действия.</strong></p>
<p>По  истечении 30 дней с даты направления уведомления договор ресурсоснабжения  прекращает действие. Одновременно начинают действовать «прямые» договоры (ч.5  ст.157.2 ЖК РФ).</p>
<p> Устанавливается  также, что заключение «прямых» договоров в письменной форме не  требуется.</p>
