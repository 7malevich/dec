	 На Дальнем Востоке чествуют предприятия, которые по итогам 2021 года стали победителями акции «Надежный партнер» и конкурса «На шаг впереди!». Торжественные мероприятия, приуроченные к 15-летию Дальневосточной энергетической компании, охватили практически весь Дальний Восток – от Сахалина до Якутска. Из 65 тысяч корпоративных клиентов и более двух тысяч Управляющих компаний и ТСЖ, ПАО «ДЭК» выявило 160 лучших предприятий. <br>
</p>
