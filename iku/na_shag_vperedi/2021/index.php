<p>
	 «Надежный Партнер» — это своеобразный знак качества, свидетельство надежности и стабильности партнерских отношений между энергетиками и потребителями энергоресурсов. Лучшие корпоративные клиенты были определены в пяти номинациях: промышленные потребители, бюджетные потребители, предприятия сельского и рыболовецкого хозяйства, предприятия малого бизнеса, предприятия сферы ЖКХ. Основными критериями отбора стали: внедрение в своем производстве энергоэффективных технологий, позволяющих снизить нагрузку на энергетические предприятия, соблюдение сроков оплат, не допущение фактов неучтенного потребления тепловой и электрической энергии, внедрение электронного документооборота с ПАО «ДЭК».
</p>
<p>
	 Для определения лучших исполнителей коммунальных услуг в конкурсе «На шаг впереди!» главными критериями стали результаты работы по внедрению участниками энергоэффективных технологий и снижение общедомовых нужд в многоквартирных домах, своевременное обеспечение передачи показаний и оплата потребленных ресурсов. Также одним из обязательных условий являлось наличие регистрации в системе и использование во взаимодействии с ПАО «ДЭК» электронного документооборота. <br>
</p>
