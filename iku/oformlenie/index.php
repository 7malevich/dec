<h4> Понуждение к заключению договора энергоснабжения </h4>
<p>
	 В соответствии с <a href="../zakony/">Правилами № 124</a>, в случае неполучения стороной, направившей заявку (оферту), в течение 30 дней со дня получения заявки (оферты) другой стороной ответа о согласии заключить договор ресурсоснабжения на предложенных условиях либо на иных условиях, соответствующих гражданскому и жилищному законодательству Российской Федерации, в том числе Правилам № 124 и нормативным правовым актам в сфере ресурсоснабжения, или об отказе от заключения договора ресурсоснабжения по основаниям, предусмотренным Правилами № 124, а также в случае получения отказа от заключения договора ресурсоснабжения по основаниям, не предусмотренным Правилами № 124, сторона, направившая заявку (оферту), вправе обратиться в суд с требованием о понуждении другой стороны, для которой заключение такого договора является обязательным, к заключению договора ресурсоснабжения.
</p>
<p>
	 При этом в случаях, указанных в пункте 21(1) Правил № 124, договор ресурсоснабжения в отношении коммунального ресурса, потребляемого при использовании общего имущества, при неполучении стороной, направившей заявку, в течение 30 дней со дня получения заявки другой стороной ответа о согласии заключить договор ресурсоснабжения на предложенных условиях либо на иных условиях, соответствующих гражданскому и жилищному законодательству Российской Федерации <strong>признается заключенным с даты направления указанной заявки</strong>.
</p>
<p>
	 В соответствии с <a href="../zakony/">пп. «г» п. 4(1) Положений о лицензировании предпринимательской деятельности по управлению многоквартирными домами, утвержденных Постановлением Правительства № 1110</a>, к грубому нарушению лицензионных требований относится такое нарушение, при котором управляющая компания, в рамках исполнения обязанностей по договору управления многоквартирным домом, в течение 30 календарных дней, со дня начала исполнения договора управления многоквартирным домом, не заключила договора с ресурсоснабжающими организациями в целях приобретения коммунальных ресурсов в многоквартирном доме.
