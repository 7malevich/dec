<h4> Существенными условиями договора энергоснабжения являются: </h4>
<p>
	 - предмет договора;
</p>
<p>
	 - дата и время начала исполнения обязательств по договору;
</p>
<p>
	 - точка (точки) поставки по договору;
</p>
<p>
	 - обязанность Исполнителя урегулировать отношения по передаче электрической энергии в отношении энергопринимающих устройств потребителя
</p>
<p>
	 - требования к качеству поставляемой электрической энергии, которые должны соответствовать требованиям законодательства Российской Федерации;
</p>
<p>
	 - порядок определения объема покупки электрической энергии (мощности) по договору за расчетный период;
</p>
<p>
	 - порядок определения стоимости, поставленной по договору за расчетный период электрической энергии (мощности);
</p>
<p>
	 - условия о порядке учета электрической энергии (мощности) с использованием приборов учета и порядке взаимодействия сторон договора в процессе такого учета;
</p>
<p>
	 - характеристики приборов учета, имеющихся на дату заключения договора;
</p>
<p>
	 - ответственность гарантирующего поставщика за нарушение условий поставки, в том числе надежности электроснабжения и качества электрической энергии, ответственность Исполнителя (Покупателя) за нарушение порядка оплаты, ответственность сторон договора за нарушение порядка полного и (или) частичного ограничения режима потребления электрической энергии;
</p>
<p>
	 - следующие права потребителя (покупателя) по договору:
</p>
<p>
	 1. Требовать поддержания на границе балансовой принадлежности электросетей показателей качества электроэнергии (ПКЭ) в соответствии с ГОСТ 32144-2013.
</p>
<p>
	 2. Требовать обеспечения надежности энергоснабжения в соответствии с условиями Договора.
</p>
<p>
	 3. Перейти на обслуживание к энергосбытовой организации при условии отсутствия задолженности по Договору, в случаях и порядке, предусмотренных действующим законодательством РФ.
</p>
<p>
	 4. Отказаться от исполнения Договора полностью в случае прекращения обязанности предоставлять электрическую энергию (мощность) потребителям. При этом Исполнитель обязан оплатить в полном объеме поставленную электрическую энергию до момента расторжения Договора, а также исполнить все возникшие до момента расторжения Договора энергоснабжения обязательства, в том числе обязательства, возникшие вследствие применения мер ответственности за нарушение условий договора. <br>
