<h4> Порядок заключения договора энергоснабжения </h4>
<p>
	 ПАО «ДЭК» в течение 30 дней со дня получения заявления о заключении договора энергоснабжения и прилагаемых документов, направляет заявителю подписанный со своей стороны проект договора. Заявитель, получивший проект договора и не имеющий возражения по его условиям, заполняет договор и направляет один подписанный экземпляр в ПАО «ДЭК».
</p>
<p>
	 В случае если заявитель не согласен с условиями, содержащимися в полученном проекте договора, он вправе направить в ПАО «ДЭК» протокол разногласий к проекту договора. В течение 10 дней ПАО «ДЭК» подписывает договор в редакции заявителя, либо принимает меры по урегулированию разногласий, и подписывает договор в согласованной с заявителем редакции либо уведомляет в письменной форме заявителя об отказе от внесения изменений с указанием причин такого отказа.
</p>
<p>
	 Если заявителем вместе с заявлением о заключении договора энергоснабжения и прилагаемыми к нему документами представлен проект договора и, если отсутствуют основания для отказа от заключения договора, ПАО «ДЭК» в течение 30 дней подписывает и передает заявителю один экземпляр представленного заявителем договора.
</p>
<p>
	 Если заявителем вместе с заявлением о заключении договора энергоснабжения представлен протокол разногласий к проекту договора, размещенному (опубликованному) Гарантирующим поставщиком в соответствии с <a href="../zakony/">пунктом 33 Основных положений функционирования розничных рынков электрической энергии, утв. ПП РФ № 442 (далее – Основные положения № 442)</a>, то Гарантирующий поставщик, если отсутствуют указанные в пункте 32 Основных положений № 442 основания для отказа от заключения договора, в течение 30 дней со дня получения заявления подписывает и передает заявителю договор в редакции заявителя, либо принимает меры по урегулированию разногласий и подписывает договор в согласованной с заявителем редакции, либо в письменной форме уведомляет заявителя об отказе от внесения предложенных изменений в проект договора с указанием причин такого отказа. При отклонении протокола разногласий либо неполучении заявителем от Гарантирующего поставщика извещения о результатах его рассмотрения в указанный срок заявитель вправе передать разногласия, возникшие при заключении договора, на рассмотрение суда. <br>
