<h3>Указания 
  <br />
 по применению тарифов на электрическую энергию 
  <br />
 (мощность) для потребителей ОАО &laquo;Дальневосточная энергетическая компания&raquo; (филиал &laquo;Хабаровскэнергосбыт&raquo;) в Хабаровском крае </h3>
 
<p> <strong>1. </strong><strong>Общие положения</strong> </p>
 
<p> Тарифы на электрическую энергию для потребителей ОАО &laquo;Дальневосточная энергетическая компания&raquo; (филиал &laquo;Хабаровскэнергосбыт&raquo;) - далее ОАО &laquo;ДЭК&raquo; в Хабаровском крае учитывают все затраты по приобретению электрической энергии (мощности) с оптового рынка, передаче и сбыту электрической энергии. </p>
 
<p> Тарифы на электрическую энергию для потребителей ОАО &laquo;ДЭК&raquo; - единые &laquo;котловые&raquo; тарифы, сформированы с учетом равенства тарифов на услуги по передаче электрической энергии для всех потребителей услуг, расположенных на территории Хабаровского края и принадлежащих к одной тарификационной группе, с учетом дифференциации по уровням напряжения. </p>
 
<p> Держателем &laquo;котла&raquo; на территории Хабаровского края определено ОАО &laquo;ДЭК&raquo;. Оплата за потребленную электрическую энергию потребителями производится ОАО &laquo;ДЭК&raquo;. В интересах потребителей электрической энергии ОАО &laquo;ДЭК&raquo; заключает с сетевыми организациями договоры на услуги по передаче электрической энергии по сетям сетевых организаций до потребителя. </p>
 
<p> Тарифы на услуги по передаче электрической энергии, утвержденные для сетевых организаций, предназначены для расчетов за оказанную услугу по транспортировке электрической энергии между ОАО &laquo;ДЭК&raquo; и сетевыми организациями. </p>
 
<p> &nbsp; </p>
 
<p> <strong>2. </strong><strong>Группы потребителей</strong> </p>
 
<p> Настоящие тарифы на электрическую энергию дифференцированы по следующим группам потребителей: </p>
 
<p> 2.1. <em><u>базовые потребители</u></em><em> - </em>потребители с максимальным значением заявленной мощности, равным или более 20 МВт, и годовым числом часов использования заявленной мощности более 7500, подтвержденным фактическим электропотреблением за предшествующий период регулирования. </p>
 
<p> 2.2.&nbsp;&nbsp; <em><u>бюджетные потребители</u> - </em>некоммерческие организации, финансируемые за счёт средств федерального бюджета и бюджетов государственных внебюджетных фондов, бюджета Хабаровского края и бюджетов территориальных государственных внебюджетных фондов, а также местных бюджетов (за исключением общественных объединений инвалидов, финансируемых <strong>из </strong>средств бюджетов различных уровней); </p>
 
<p> -&nbsp;&nbsp;&nbsp; исполнители коммунальных услуг (товарищества собственников жилья, жилищно строительные, жилищные или иные специализированные потребительские кооперативы либо управляющие организации), наймодателей (или уполномоченных ими лиц), предоставляющих гражданам жилые помещения специализированного жилищного фонда: жилые помещения ; общежитиях, жилые помещения маневренного фонда, жилые помещения в домах системы социального обслуживания населения, жилые помещения фонда для временного поселения вынужденных переселенцев, для временного поселения лиц, признанных беженцами, жилых помещения для социальной защиты отдельных категорий граждан, приобретающие электроэнергии для коммунально-бытового потребления граждан в объемах фактического потребление потребителей-граждан и объемах электроэнергии, израсходованной на места общего пользования; </p>
 
<p> -&nbsp;&nbsp; садоводческие, огороднические или дачные некоммерческие объединения граждан -некоммерческие организации, учрежденные гражданами на добровольных началах для содействия ее членам в решении общих социально-хозяйственных задач ведения садоводства, огородничества и дачного хозяйства; </p>
 
<p> - некоммерческие объединения граждан (гаражно-строительные, гаражные кооперативы); </p>
 
<p> - населенные пункты, объединенные хозяйственные постройки граждан (погреба, сараи и иные сооружения), жилые зоны при воинских частях и исправительно-трудовых учреждениях, (жилые дома, общежития, в которых проживает обслуживающий персонал, работники, их семьи), при условии наличия раздельного учета на указанные объекты; </p>
 
<p> В соответствии с пунктом 2 Постановления Правительства Российской Федерации от 7 декабря 1998 г. N 1444 &quot;Об основах ценообразования в отношении электрической энергии, потребляемой населением&quot; для населения, проживающего в сельских населенных пунктах, а также в городских населенных пунктах в домах, оборудованных в установленном порядке стационарными электроплитами и электроотопительными установками, применяется понижающий коэффициент 0,7. </p>
 
<p> 2.4. <em><u>прочие потребители</u></em> - потребители электрической энергии, не указанные в подпунктах 2.1., 2.2., 2.3. Указаний. </p>
 
<p> С учётом особенностей структуры электропотребления и частичного финансирования за счёт бюджетных средств из числа прочих потребителей выделяются: </p>
 
<p> сельскохозяйственные товаропроизводители - физические или юридические лица, осуществляющие производство сельскохозяйственной продукции. Список (перечень) сельскохозяйственных товаропроизводителей формируется Правительством Хабаровского края по представлению Министерства сельского хозяйства Хабаровского края; </p>
 
<p> -&nbsp; художественные мастерские, религиозные организации, предприятия ритуальных услуг, общественные объединения инвалидов, муниципальные бани; </p>
 
<p> -&nbsp; котельные - предприятия, осуществляющие выработку тепловой энергии, поставляемую населению края; предприятия водопроводно - канализационного хозяйства - предприятия, оказывающие услуги водоснабжения и водоотведения населению края. Список (перечень) котельных и предприятий водопроводно &mdash; канализационного хозяйства формируется Правительством края по представлению Министерства жилищно-коммунального хозяйства края. </p>
 
<p> -&nbsp; объекты культуры - объекты, представляющие культурную ценность. Список объектов культуры формируется Правительством края. </p>
 
<p> - потребители, опосредованно присоединенные к территориальным распределительным сетям; </p>
 
<p> - детские загородные оздоровительные центры (на период июнь - август). </p>
 
<p> &nbsp; </p>
 
<p> <strong>3. </strong><strong>Виды тарифов на электрическую энергию</strong> </p>
 
<p> 3.1. Тарифы на электрическую энергию, поставляемую потребителям, установлены: </p>
 
<p> 3.1.1. одноставочные, в виде одной ставки, включающей в себя полную стоимость покупки&nbsp; 1 киловатт-часа (кВтч) электрической энергии; </p>
 
<p> 3.1.2. двухставочные, в виде двух ставок, включающих в себя ставку платы за покупку 1 киловатта (кВт) заявленной потребителем в договоре энергоснабжения мощности, участвующей в максимуме нагрузки энергосистемы (основная ставка) и платы за 1 киловатт час (кВтч) отпущенной потребителю электрической энергии (дополнительная ставка); </p>
 
<p> 3.1.3. зонные тарифы, предусматривающие дифференцированную стоимость покупки 1 киловатт часа электрической энергии по суточным зонам графика электрической нагрузки. </p>
 
<p> 3.2. Двухставочные тарифы могут применяться для оплаты потребленной энергии и мощности, если в договорах энергоснабжения оговорена заявленная мощность. </p>
 
<p> Заявленная мощность - наибольшая получасовая электрическая мощность, совпадающая с периодом максимальной нагрузки энергосистемы, используемая на производственные нужды потребителя. </p>
 
<p> 3.3.&nbsp;&nbsp; Интервалы тарифных зон суток (пик, полупик, ночь) по энергозонам устанавливаются Федеральной службой по тарифам на основании информации, запрашиваемой в ОАО &laquo;Системный оператор-Центральное диспетчерское управление Единой энергетической системы России&raquo;. </p>
 
<p> 3.4.&nbsp;&nbsp; Опосредованным присоединением потребителей к территориальным распределительным сетям считается случай, когда потребитель имеет возможность электроснабжения от территориальной распределительной сети через энергетические установки ТЭЦ. Такой территориальной распределительной сетью на территории Хабаровского края определено ОАО &laquo;Дальневосточная распределительная сетевая организация&raquo; (филиал &laquo;Хабаровские электрические сети&raquo;) - далее ОАО &laquo;ДРСК&raquo;. Потребители, опосредованно присоединенные к электрическим сетям ОАО &laquo;ДРСК&raquo;, оплачивают услуги по передаче электрической энергии по ставке на содержание сетей, рассчитанной для ОАО &laquo;ДРСК&raquo;. </p>
 
