 <strong>Внимание!</strong> 03.11.2015 принят Федеральный закон РФ №307-ФЗ «О внесении изменений в отдельные законодательные акты Российской Федерации в связи с укреплением платежной дисциплины потребителей энергетических ресурсов». Согласно закону, с 01.01.2016 ужесточены штрафные санкции (пени) в отношении потребителей, нарушающих обязательства по оплате электрической и тепловой энергии. Более суровые условия призваны стимулировать потребителя не тянуть с оплатой счетов, так как каждый день просрочки значительно увеличивает сумму к оплате.
