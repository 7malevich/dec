</p>
<p>
	 Федеральный закон РФ №307-ФЗ вносит изменения и в Административный кодекс РФ. В частности, ужесточены санкции по статье 7.19 КоАП РФ. Штрафы за безучетное и бездоговорное потребление энергоресурсов увеличены втрое и составляют для граждан от 10 до 15 тысяч рублей, для должностных лиц от 30 до 80 тысяч рублей (или дисквалификация до 2 лет), для юридических от 100 до 200 тысяч рублей.
</p>
<p>
