<h3 class="sp-hdr">1.3. Функции регионального оператора? </h3></div>
<div class="spoiler-text">
<p>В  соответствии с Федеральным законом от 24 июня 1998 года № 89-ФЗ «Об отходах  производства и потребления» региональный оператор обеспечивает сбор,  транспортирование, обработку, утилизацию, обезвреживание, размещение твердых  коммунальных отходов (далее – ТКО) в зоне деятельности регионального оператора  на территории субъекта Российской Федерации в соответствии с региональной  программой в области обращения с отходами и территориальной схемой обращения с  отходами. </p>
<p> Региональный  оператор работает самостоятельно или с привлечением операторов по обращению с  ТКО. Статус регионального оператора присваивается на срок не более чем десять  лет на основании конкурсного отбора. <br />
</p>
</div>

