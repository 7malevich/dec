<div class="spoiler-text">
<p> ПАО «ДЭК» осуществляет начисление  платы клиентам – физическим лицам по реестрам Региональных операторов, печать,  доставку квитанций, сбор платы и консультирование населения по способам оплаты,  задолженности, по перерасчетам. Кроме того, ПАО «ДЭК» будет принимать обращения  граждан с жалобами на качество оказания услуг для передачи Региональному  оператору. В случае подтверждения некачественного оказания услуги обратившемуся  гражданину будет сделан перерасчет.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">3.2. На  каком основании я должен оплачивать услугу по обращению с ТКО филиалу ПАО «ДЭК»  «Амурэнергосбыт»?</h3></div>
<div class="spoiler-text">
<p>В данном случае ПАО «ДЭК» оказывает  услугу не населению, а Региональным операторам по обращению с ТКО.</p>
<p>Филиал выбран операторами для  оказания услуг по начислению платы, печати и доставке квитанций, приёму  платежей от населения, перечислению средств на счета региональных операторов по  обращению с ТКО и консультированию потребителей.</p>
<p> Между Региональными операторами и  ПАО «ДЭК» заключены договоры на оказание соответствующих услуг.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">3.3. Можно ли не платить за вывоз  мусора, если его нет (закапываю на своем участке и сжигаю сам)?</h3></div>
<div class="spoiler-text">
<p>  К сожалению, для собственников жилых  домов заключение договоров на услуги по обращению с ТКО обязательно.  Накапливать твердые коммунальные отходы можно только в специально оборудованных  местах. Такие площадки должны отвечать требованиям по охране окружающей среды и  санэпидемнормам. Сжигать отходы без специального оборудования, которое очищает  выбросы, запрещено. Плюс для обращения с отходами I-IV классов опасности  требуется наличие лицензии. За нарушение всех этих правил могут штрафовать  собственника дома или земельного участка. Пожалуй, проще всего будет все-таки  заключить договор с региональным оператором.</p>
<p> <em><strong>Важно:</strong> при  обнаружении несанкционированного места складирования ТКО объемом более 1  кубометра региональный оператор обязан пожаловаться в уполномоченные органы и  высылать собственнику земельного участка уведомление о необходимости ликвидации  этой свалки. Собственнику участка надо будет самостоятельно ликвидировать  свалку или заключить договор с региональным оператором в течение 30 дней. Если  свалка так и не будет ликвидирована, региональный оператор устранит ее сам и  через суд взыщет с собственника участка свои расходы. Так написано в пунктах  16-17 ПП РФ №1156. </em></p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">3.4. Кому предъявлять претензии по  некачественному вывозу мусора?</h3></div>
<div class="spoiler-text">
<p>  - Управляющей компании / ТСЖ /  кооперативу — если договор на услуги по вывозу ТКО с региональным оператором  заключали они. Претензии по содержанию и чистоте контейнерных площадок,  мусоропровода, мусороприемной камеры нужно также писать в УК. <br />
  - Региональному оператору ТКО — если  у вас заключен договор напрямую с ним. Ответственность регионального оператора  начинается с момента погрузки отходов в мусоровоз на местах сбора и накопления  ТКО (контейнерных и специальных площадках). </p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">3.5. Кто  ответственен за содержание контейнерных площадок?</h3></div>
<div class="spoiler-text">
<p>  Содержание и обустройство  контейнерных площадок осуществляют собственники земельного участка, на котором  они расположены. <br />
  Организация по обслуживанию  жилищного фонда (УК, ТСЖ, ЖСК) на основании заключенного договора на  обслуживание обязана обеспечивать содержание в исправном состоянии контейнеров  без переполнения и загрязнения территории. <br />
Там, где собственник не определен и отсутствует  управляющая организация, обязанность по содержанию и обустройству возлагается  на органы местного самоуправления. </p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">3.6. Кто  должен содержать контейнерные площадки и проводить на них уборку?</h3></div>
<div class="spoiler-text">
<p>Собственники  земельного участка.</p>
<p> Если контейнерная площадка и территория, прилегающая к месту погрузки мусора,  расположена на придомовой территории, входящей в состав общего имущества  собственников МКД, то обязанность по уборке лежит на собственниках данного МКД  и уборка будет проводиться в зависимости от выбранного ими способа управления  домом.</p>
<p> Ответственность регионального оператора возникает с момента погрузки мусора в  мусоровоз. Убирать контейнерные площадки, а так же собирать мусор, который  находится вне контейнеров он не обязан и не будет, за исключением  крупногабаритных отходов со специальных площадок.</p>
</div>

