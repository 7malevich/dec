<h3 class="sp-hdr">4.3.  Если ни с кем не заключать договор на вывоз мусора, но пришлют квитанции.  Законно ли это?</h3></div>
<div class="spoiler-text">
<p>Да, это законно. Есть 3 варианта: </p>
<p> 1.  Вы живете в многоквартирном доме с УК / ТСЖ / кооперативом и платите за мусор  им. Вам не нужно заключать отдельный договор на обращение с ТКО. Его заключает  Ваша управляющая компания (ТСЖ, жилищный кооператив). УК выставляет Вам  квитанции на оплату услуг по обращению с ТКО, вы платите УК, УК рассчитывается  за оказанные услуги с региональным оператором ТКО. </p>
<p> 2.  Вы живете в многоквартирном доме с УК /  ТСЖ / кооперативом, но платите региональному оператору. Если общее  собрание собственников решило платить напрямую поставщикам коммунальных услуг,  тогда квитанции за обращение с ТКО вам будет выставлять региональный оператор.  Вы должны будете оплачивать их напрямую региональному оператору. Договор на  обращение с ТКО в этом случае заключается каждым собственником с региональным  оператором. </p>
<p> 3.  В вашем доме непосредственное  управление / у вас частный дом. В этом случае вы сами должны будете  заключить договор на обращение с ТКО с региональным оператором и платить ему  напрямую по выставленным квитанциям. </p>
<p> Собственники  твердых коммунальных отходов обязаны заключить договор на оказание услуг по  обращению с твердыми коммунальными отходами с региональным оператором. </p>
<p> Прямой  договор в обязательном порядке заключают юридические лица и индивидуальные  предприниматели. Жителям многоквартирных домов (МКД) и частного сектора услуга  может предоставляться в рамках договора публичной оферты или путем совершения  конклюдентных действий, когда договор считается заключенным, если стороны без  заключения письменного договора фактически совершают обусловленные согласием  сторон действия: жители складируют ТКО в установленных местах накопления, а  региональный оператор осуществляет вывоз ТКО из этих мест накопления. </p>
<p> Текст  типового договора установлен постановлением Правительства Российской Федерации  от 12.11.2016 № 1156 и опубликован на сайте регионального оператора. По итогам  первого месяца работы региональный оператор направляет жителям квитанции на  оплату услуги. При отсутствии письменных обращений о непредоставлении услуги  квитанция подлежит оплате в полном объеме. </p>
<p> Жителям  многоквартирных домов (МКД) коммунальная услуга по обращению с ТКО может быть  оказана на основании прямого договора с каждым из жителей (если такое решение  принято на общем собрании жильцов) либо через управляющую МКД организацию (УК,  ТСЖ, ЖСК). При наличии управляющей организации она обязана заключить договор с  региональным оператором. </p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">4.4. Если у  собственника частного жилого дома уже заключен и имеется договор на вывоз ТКО,  должен ли он заключаться договор с региональным оператором ТКО?</h3></div>
<div class="spoiler-text">
<p>С  01.01.2019 г. деятельность по обращению с ТКО имеют право осуществлять только  региональные операторы и все собственники ТКО обязаны заключить договор на  оказание услуг по обращению с ТКО именно с региональным оператором. Остальные  договоры утратят законную силу, т.е будут недействительными. Так же обращаем  внимание на то, что самостоятельное транспортирование (т.е. вывоз) ТКО  запрещено.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">4.5. Почему я должен заключать договор с региональным  оператором?</h3></div>
<div class="spoiler-text">
<p>Частью  5 статьи 30 Жилищного кодекса Российской Федерации установлено, что собственник  жилого дома или части жилого дома обязан обеспечивать обращение с ТКО путем  заключения договора с региональным оператором.</p>
<p> В соответствии со статьей 24.7 Федерального закона от 24.06.1998 № 89-ФЗ  &quot;Об отходах производства и потребления&quot; собственники ТКО обязаны  заключить договор на оказание услуг по обращению с ТКО с региональным  оператором, в зоне деятельности которого образуются ТКО и находятся места их  накопления, и оплачивать услуги регионального оператора по цене, определенной в  пределах утвержденного тарифа на услугу регионального оператора. Такой договор  является публичным для регионального оператора.</p>
</div>

<h2 style="margin-top:50px">5.  РАСЧЕТЫ</h2>

<div class="spoiler folded">
<h3 class="sp-hdr">5.1.  Почему начисление платы за обращение с ТКО производится исходя из общей площади  жилого помещения?</h3></div>
<div class="spoiler-text">
<p>Постановлением  Правительства Амурской области от 25.02.2019г. №58 установлено, что на  территории Амурской области оплата коммунальной услуги по обращению с твердыми коммунальными  отходами осуществляется потребителями исходя из общей площади жилого помещения  в соответствии с абзацем вторым пункта 148(30) Правил предоставления  коммунальных услуг собственникам и пользователям помещений в многоквартирных  домах и жилых домов, утвержденных постановлением Правительства Российской  Федерации от 6 мая 2011 г. N 354 &quot;О предоставлении коммунальных услуг  собственникам и пользователям помещений в многоквартирных домах и жилых  домов&quot;.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">5.2. От чего будет зависеть плата за ТКО у собственников жилых  помещений?</h3></div>
<div class="spoiler-text">
<p>Для  граждан размер платы за данную коммунальную услугу будет зависеть от тарифа (у  каждого регионального оператора он свой и действует на всю зону (кластер)  ответственности регионального оператора), норматива накопления ТКО на один  квадратный метр жилого помещения и от площади жилого помещения. Тариф  региональным операторам устанавливает управление государственного регулирования  цен и тарифов Амурской области.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">5.3. Как производится расчет платы  за обращение с ТКО?</h3></div>
<div class="spoiler-text">
<p>Размер  платы за &quot;Обращение с ТКО&quot; определяется исходя из общей площади  жилого помещения (Постановление Правительства АО от 25.02.19 №58), по формуле  9(2) Приложения 2 ПП РФ от 06.05.2011 №354: <br />
  <strong>Pi=Si*Ni/12*Tотх</strong>,  где:<br />
  Si-общая  площадь помещения;<br />
  Ni-норматив  накопления ТКО (Постановление Правительства АО от 18.10.18 №502), для  благоустроенных жилых помещений <strong>0,1062  куб.м./год, </strong>неблагоустроенных жилых помещений с печным отоплением <strong>0,0531 куб.м./год;</strong><br />
Tотх-тариф  для соответствующего регионального оператора.</p>
</div>

