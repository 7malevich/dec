<h2 style="margin-top:50px">2. ТВЕРДЫЕ  КОММУНАЛЬНЫЕ ОТХОДЫ (ТКО)</h2>

<div class="spoiler folded">
<h3 class="sp-hdr">2.1. Какие  отходы относятся к твердым коммунальным?</h3></div>
<div class="spoiler-text">
<p> Твердые коммунальные отходы, говоря  простым языком - это остатки продуктов и предметы, использовавшиеся в быту и  утратившие свои потребительские свойства более известные как ТБО, а также  отходы, схожие по свойствам и составу с ТБО, но образовавшиеся в процессе  деятельности юридических лиц, индивидуальных предпринимателей.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">2.2. Что конкретно включается в услугу по  обращению с ТКО?</h3></div>
<div class="spoiler-text">
<p> В услугу по обращению с твердыми  коммунальными отходами входят сбор, транспортирование, обезвреживание и  захоронение ТКО.</p>
</div>

<div class="spoiler folded">
<h3 class="sp-hdr">2.3. Как часто должны вывозить мусор от  многоквартирных домов?</h3></div>
<div class="spoiler-text">
<p>  В холодное время года  (среднесуточная температура +5 °C и ниже) — не реже 1 раза в 3 суток. В теплое  время года (среднесуточная температура выше +5 °C) — 1 раз в сутки. В течение  одного месяца допустимо отклонение от этой нормы не более 72 часов.  Единовременно в холодное время года вывоз мусора могут задержать не более чем  на 48 часов, в теплое время года — не более чем на 24 часа. Эти нормативы  указаны в приложении 1 к ПП РФ №354. </p>
<table class="table-data2">
  <tr>
    <td nowrap="nowrap"><br />
