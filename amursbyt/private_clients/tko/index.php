  <strong>Pi=Si*Ni/12*Tотх</strong>,  где:<br />
  Si-общая  площадь помещения;<br />
  Ni-норматив  накопления ТКО (Постановление Правительства АО от 18.10.18 №502), для  благоустроенных жилых помещений <strong>0,1062  куб.м./год, </strong>неблагоустроенных жилых помещений с печным отоплением <strong>0,0531 куб.м./год;</strong><br />
Tотх-тариф  для соответствующего регионального оператора.</p>
