      </div>
  </li>
  <li>
    <div align="justify">садоводческие,  огороднические или дачные некоммерческие   объединения граждан - некоммерческие  организации, учрежденные   гражданами на добровольных началах для содействия ее  членам в решении   общих социально-хозяйственных задач ведения садоводства,  огородничества   и дачного хозяйства;<br />
      </div>
  </li>
  <li>
    <div align="justify">некоммерческие  объединения граждан (гаражно-строительные, гаражные кооперативы, автостоянки);<br />
      </div>
  </li>
  <li>
    <div align="justify">населенные пункты,  объединенные хозяйственные постройки граждан   (погреба, сараи и иные  сооружения), жилые зоны при воинских частях и   исправительно-трудовых  учреждениях, рассчитывающиеся по общему счетчику   на вводе;<br />
