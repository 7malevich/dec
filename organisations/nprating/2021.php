России, в том числе  Дальнего Востока,  приглашаются в Москву  для  участия в Торжественной церемонии награждения победителей конкурса.</p>
<p> По итогам регионального этапа конкурса список победителей  направляется в адрес оргкомитета федеральной акции «Надёжный партнёр» для  формирования общероссийского рейтинга и определения лучших энергопотребителей  страны.</p>
<p>Организатором  конкурса в регионе выступает ПАО «ДЭК» при поддержке администраций Приморского, Хабаровского края, Еврейской АО, Амурской и Сахалинской областей.</p>

