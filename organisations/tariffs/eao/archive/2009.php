<h3 align="center">Указания
  <br />
 по применению тарифов на электрическую энергию
  <br />
 (мощность) для потребителей, получающих электрическую
  <br />
 энергию от гарантирующего поставщика розничного рынка
  <br />
 электрической энергии Еврейской автономной области
  <br />
 ОАО &quot;Дальневосточная энергетическая компания&quot;</h3>
 
<h4 align="center">I. Общие положения</h4>
 
<p> Тарифы на электрическую энергию, поставляемую всем потребителям Еврейской автономной области, учитывают затраты на покупку, передачу и сбыт энергии. </p>
 
<h4 align="center">II. Группы потребителей</h4>
 
<p> Тарифы на электрическую энергию подразделяются на следующие группы: </p>
 
<p> 1. Население, в том числе: </p>
 
<p> 1.1. Городское: </p>
 
<p> 1.1.1. Население, проживающее в домах, оборудованных газовыми плитами. </p>
 
<p> 1.1.2. Население, проживающее в домах, оборудованных в установленном порядке стационарными электрическими плитами и (или) электроотопительными установками. </p>
 
<p> 1.2. Сельское. </p>
 
<p> 1.3. Потребители, приравненные к населению, в том числе: </p>
 
<p> 1.3.1. Потребители городских населенных пунктов. </p>
 
<p> 1.3.2. Потребители сельских населенных пунктов. </p>
 
<p> В соответствии с Методическими указаниями, утвержденными Приказом ФСТ России от 06.08.2004 N 20-э/2 &quot;Об утверждении методических указаний по расчету регулируемых тарифов и цен на электрическую (тепловую) энергию на розничном (потребительском) рынке, к группе &quot;Потребители, приравненные к населению&quot; относятся следующие приравненные к категории населения потребители: </p>
 
<ul>
  <li> исполнители коммунальных услуг (товарищества собственников жилья, жилищно-строительные, жилищные или иные специализированные потребительские кооперативы либо управляющие организации), наймодатели (или уполномоченные ими лица), предоставляющие гражданам жилые помещения специализированного жилищного фонда: жилые помещения в общежитиях, жилые помещения маневренного фонда, жилые помещения в домах системы социального обслуживания населения, жилые помещения фонда для временного поселения вынужденных переселенцев, для временного поселения лиц, признанных беженцами, жилые помещения для социальной защиты отдельных категорий граждан, приобретающих электроэнергию для коммунально-бытового потребления граждан в объемах фактического потребления потребителей-граждан и объемах электроэнергии, израсходованной на места общего пользования;</li>

  <li> гарантирующие поставщики, энергосбытовые, энергоснабжающие организации, приобретающие электроэнергию в целях дальнейшей продажи потребителям-гражданам и (или) исполнителям коммунальной услуги электроснабжения, наймодателям (или уполномоченным ими лицам) в объемах фактического потребления потребителей-граждан и объемах электроэнергии, израсходованной на места общего пользования;</li>

  <li> садоводческие, огороднические или дачные некоммерческие объединения граждан - некоммерческие организации, учрежденные гражданами на добровольных началах для содействия ее членам в решении общих социально-хозяйственных задач ведения садоводства, огородничества и дачного хозяйства (далее - садоводческое, огородническое или дачное некоммерческое объединение);</li>

  <li> некоммерческие объединения граждан (гаражно-строительные, гаражные кооперативы, автостоянки);</li>

  <li> населенные пункты, объединенные хозяйственные постройки граждан (погреба, сараи и иные сооружения), жилые зоны при воинских частях и исправительно-трудовых учреждениях, рассчитывающиеся по общему счетчику на вводе;</li>

  <li> религиозные организации, содержащиеся за счет прихожан.</li>
</ul>
 
<p> 2. Прочие потребители, из них: </p>
 
<p> 2.1. Иные прочие потребители - потребители электрической энергии, не указанные в подпунктах 1 настоящей главы. </p>
 
<p> 2.2. Бюджетные потребители - некоммерческие организации, финансируемые за счет средств федерального бюджета и бюджетов государственных внебюджетных фондов, бюджета Еврейской автономной области и бюджетов территориальных государственных внебюджетных фондов, а также бюджетов муниципальных образований области. </p>
 
<h4 align="center">III. Виды тарифов на электрическую энергию</h4>
 
<p> 1. Тарифы на электрическую энергию, поставляемую потребителям, установлены: </p>
 
<p> - одноставочный тариф, который включает в себя полную стоимость 1 киловатт-часа поставляемой электрической энергии и дифференцируется по уровню напряжения и зонам суток; </p>
 
<p> - двухставочный тариф, который включает в себя плату за: </p>
 
<p> а) 1 киловатт-час электрической энергии; </p>
 
<p> б) 1 киловатт электрической мощности. </p>
 
