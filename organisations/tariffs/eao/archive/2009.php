<p> 3. Потребители при наличии соответствующих технических средств учета самостоятельно выбирают для проведения расчетов за электрическую энергию на расчетный период регулирования один из вариантов тарифов тарифного меню, предварительно уведомив об этом организацию, поставляющую ему электрическую энергию, не менее чем за месяц до вступления в установленном порядке в силу указанных тарифов. При отсутствии такого уведомления за электрическую энергию (если иное не будет установлено по взаимному соглашению сторон) производится по варианту тарифа, действовавшему в период, предшествующий расчетному. В расчетном периоде регулирования не допускается изменение варианта тарифа, если иное не будет установлено по взаимному соглашению сторон. </p>
 
<p> Выбор технических средств учета электрической энергии, их установка и прием в эксплуатацию, в том числе для коммерческих расчетов, производятся по согласованию с энергоснабжающей организацией в установленном порядке. </p>
 
<h4 align="center">IV. Особенности применения тарифов на
  <br />
 электрическую энергию, поставляемую потребителям
  <br />
 Еврейской автономной области</h4>
 
<p> 1. Потребители, присоединенные к сетям энергораспределительных организаций, предоставляющих ОАО &quot;Дальневосточная энергетическая компания&quot; услуги по передаче электрической энергии, оплачивают электрическую энергию по тарифам соответствующих групп потребителей, утвержденным настоящим приказом. </p>
 
<p> 2. Расчеты за электрическую энергию, потребляемую жилищными организациями на технические цели жилых домов, производятся: </p>
 
<ul>
  <li>в городской местности - по тарифам для городского населения, проживающего в домах, оборудованных газовыми плитами (тариф утвержден с налогом на добавленную стоимость);</li>

  <li>в сельской местности - по тарифам для сельского населения (тариф утвержден с налогом на добавленную стоимость).</li>
</ul>
 3. Расчеты за электрическую энергию, потребляемую учреждениями религиозного культа, производятся в соответствии с договорами электроснабжения, заключенными в установленном порядке.  			 		 				 		<span class="article_seperator">
