<p align="center"> <strong>Указания </strong> </p>
 
<p align="center"> по применению тарифов на электрическую энергию (мощность) для потребителей, получающих электрическую энергию от гарантирующего поставщика розничного рынка электрической энергии Еврейской автономной области ОАО &laquo;Дальневосточная энергетическая компания&raquo; филиал &laquo;Энергосбыт ЕАО&raquo;. </p>
 
<p align="center">   </p>
 
<p align="center"> <strong>I . Общие положения. </strong> </p>
 
<p> Тарифы на электрическую энергию, поставляемую всем потребителям Еврейской автономной области, учитывают затраты на покупку, передачу и сбыт энергии. </p>
 
<p align="center"> <strong>II . Группы потребителей. </strong> </p>
 
<p> Тарифы на электрическую энергию подразделяются на следующие группы: </p>
 
<p> 1. Население, в том числе: </p>
 
<p> &bull;  Городское: </p>
 
<p> - Население, проживающее в домах, оборудованных газовыми плитами. </p>
 
<p> - Население, проживающее в домах, оборудованных в установленном порядке стационарными электрическими плитами и (или) электроотопительными установками. </p>
 
<p> &bull;  Сельское. </p>
 
<p> &bull;  Потребители, приравненные к населению, в соответствии с Методическими указаниями, утвержденными приказом ФСТ России от 06.08.2004 г. № 20-э/2 &laquo;Об утверждении методических указаний по расчету регулируемых тарифов и цен на электрическую (тепловую) энергию на розничном (потребительском) рынке. </p>
 
<p> 2. Прочие потребители, из них: </p>
 
<p> 2.1. Иные прочие потребители &ndash; потребители электрической энергии, не указанные в подпунктах 1 настоящей главы. </p>
 
<p> 2.2. Бюджетные потребители - некоммерческие организации, финансируемые за счет средств федерального бюджета и бюджетов государственных внебюджетных фондов, бюджета Еврейской автономной области и бюджетов территориальных государственных внебюджетных фондов, а так же бюджетов муниципальных образований области. </p>
 
<p>   </p>
 
<p align="center"> <strong>III . Виды тарифов на электрическую энергию. </strong> </p>
 
<p> 1. Тарифы на электрическую энергию, поставляемую потребителям, установлены: </p>
 
<p> - одноставочный тариф, который включает в себя полную стоимость 1 киловатт-часа поставляемой электрической энергии и дифференцируется по уровню напряжения и зонам суток; </p>
 
<p> &bull;  двухставочный тариф, который включает в себя плату за: </p>
 
<p> а) 1 киловатт-час электрической энергии; </p>
 
<p> б) 1 киловатт электрической мощности. </p>
 
